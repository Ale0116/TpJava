package model;

public class Tipo_Colectivo {

		private int idTipoColectivo;
		private int capacidad;
		private char descripción;
		
		public int getIdTipoColectivo() {
			return idTipoColectivo;
		}
		public void setIdTipoColectivo(int idTipoColectivo) {
			this.idTipoColectivo = idTipoColectivo;
		}
		public int getCapacidad() {
			return capacidad;
		}
		public void setCapacidad(int capacidad) {
			this.capacidad = capacidad;
		}
		public char getDescripción() {
			return descripción;
		}
		public void setDescripción(char descripción) {
			this.descripción = descripción;
		}
		
		

}
